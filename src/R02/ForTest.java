package R02;

public class ForTest {
    // Ilustracja uzycia petli for
    public static void main(String[] args) {
        int x;

        for(x =0; x<=15; x++)
            System.out.println("Wartość x wynosi " +x);
    }
}
/*
    for(inicjalizacja; warunek; iteracja)instrukcja;

        inicjalizacja sluzy do ustawniwnia wartości początkowej;

        warunek to wyrażenie logiczne, które sprawdza zmienną sterującą;

        jeżeli warunek jest prawdziwy, to wykonywana jest instrukcja;

        pętla przechodzi do kolejnej iteracji.
 */
